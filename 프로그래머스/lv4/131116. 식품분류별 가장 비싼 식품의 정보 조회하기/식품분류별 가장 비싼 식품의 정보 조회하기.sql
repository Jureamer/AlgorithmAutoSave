    CATEGORY, 
    PRICE,
    PRODUCT_NAME
FROM 
    FOOD_PRODUCT
WHERE 
    CATEGORY REGEXP '과자|국|김치|식용유'
    AND PRICE IN (SELECT MAX(PRICE) p
    FROM FOOD_PRODUCT
    GROUP BY CATEGORY) 
